// ####### L'addiction ###### //

// -- Déclaration de plusieurs variable a la suite 
var nb1, nb2, resultat;

nb1 = 10;
nb2 = 5;

// -- Addition de nb1 + nb2 avec l'operateur "+" 
resultat = nb1 + nb2;

// -- affichage du resultat dans la console 
console.log(resultat);

// #### Soustraction ##### //

// -- soustraction de nb1 - nb2 avec l'operateur "-"
resultat = nb1 - nb2;

// -- affichage du resultat dans la console 
console.log(resultat);


// ####### multiplication ####### //

// -- multiplication de nb1 et nb2 avec l'operateur "*"
resultat = nb1 * nb2;

//-- affichage du resultat dans la console 
console.log(resultat);

// ###### la division ##### // 

//-- division de nb1 et nb2 avec l'operateur "/"
resultat = nb1 / nb2;

//-- affichage du resultat dans la console
console.log(resultat);

// ###### le modulo ###### //

// -- modulo de nb1 et nb2 avec l'operateur "%"
nb1 = 11;
resultat = nb1 % nb2;

// affichage du resultat dans la console.
console.log( "le reste de la divison de " + nb1 + " par " + nb2 + " est egal a : " + resultat);

